#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*temp;

	if (!del || !lst || !*lst)
		return ;
	while (lst && *lst)
	{
		temp = *lst;
		*lst = (*lst)->next;
		ft_lstdelone(temp, (*del));
	}
}
