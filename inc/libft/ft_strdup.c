#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*str;
	size_t	len;

	len = ft_strlen(s);
	str = (char *)ft_calloc(len + 1, 1);
	if (!str)
		return (NULL);
	return (ft_memcpy(str, s, len));
}
