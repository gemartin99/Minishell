#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	int		i;
	t_list	*temp;

	temp = lst;
	i = 0;
	while (i < ft_lstsize(lst) - 1)
	{
		i++;
		temp = temp->next;
	}
	return (temp);
}
