#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	size_t	i;

	if (!src && !dst)
		return (NULL);
	i = 0;
	if (src < dst)
	{
		while (++i <= len)
			((char *)dst)[len - i] = ((char *)src)[len - i];
	}
	else
		ft_memcpy(dst, src, len);
	return (dst);
}
